<p>About diet planner
Website focused on providing tools and support for people who want to take control over their nutrition. Given the saturation of information in the diet industry, we focus on more pragmatic elements of healthy eating such as planning, shopping and cooking.

This site is latest part of our ongoing project to make effective nutrition strategies available to everyone, especially people who are too busy to get started on their own. What began as Swole.me, a simple tool to help bodybuilders hit macronutrient requirements created by just Louis, has grown into the current service supported by a small and dedicated team.
